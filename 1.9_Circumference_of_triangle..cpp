/*
circumference of triangle = a+b+c 
*/
#include<stdio.h>
main()
{
	int a=20,b=45,c=33,circumference;
	circumference = a+b+c;
	
	printf("\n\n\t a = %d",a);
	printf("\n\n\t b = %d",b);
	printf("\n\n\t c = %d",c);
	
	printf("\n\n\t circumference = %d",circumference);
	
	
	printf("\n\n\t %d = %d+%d+%d",circumference,a,b,c);
		
}
